    public async Task<long> InsertAsync(User user, CancellationToken cancellationToken = default)
    {
        try
        {
            IQueryBuilder<UserEntity, object> queryBuilder = new QueryBuilder<UserEntity, object>();
            var sql = queryBuilder
                .Insert(() => new UserEntity
                {
                    Username = user.Username,
                    Email = user.Email,
                    PasswordHash = user.PasswordHash,
                    FullName = user.FullName,
                    ImageUrl = user.ImageUrl,
                    Role = user.Role,
                    RefreshToken = user.RefreshToken,
                    Delt = "N",
                    IsActive = user.IsActive ? "Y" : "N"
                })
                .BuildQuery()
                + "\nRETURNING id;";

            sql = NormalizeSqlForUserTable(sql);
            _logger.LogInformation("Insert user SQL generated by IQueryBuilder: {Sql}", sql);

            using var connection = _connectionFactory.CreateOpenConnection();
            var command = new CommandDefinition(sql, cancellationToken: cancellationToken);
            return await connection.ExecuteScalarAsync<long>(command);
        }
        catch (Exception ex)
        {
            _logger.LogError(ex, "InsertAsync failed for email: {Email}", user.Email);
            throw;
        }
    }

