    public async Task<User?> GetByIdAsync(long userId, CancellationToken cancellationToken = default)
    {
        try
        {
            IQueryBuilder<UserEntity, object> queryBuilder = new QueryBuilder<UserEntity, object>();
            var sql = queryBuilder
                .Select(u => new { u.Id, u.Username, u.Email, u.PasswordHash, u.FullName, u.ImageUrl, u.Role, u.RefreshToken, u.Delt, u.IsActive })
                .Where(u => u.Id == userId)
                .BuildQuery();

            sql = NormalizeSqlForUserTable(sql);
            _logger.LogInformation("GetById user SQL generated by IQueryBuilder: {Sql}", sql);

            using var connection = _connectionFactory.CreateOpenConnection();
            var command = new CommandDefinition(sql, cancellationToken: cancellationToken);
            var row = await connection.QueryFirstOrDefaultAsync<UserEntity>(command);

            return row is null || row.Delt == "Y"
                ? null
                : MapToDomain(row);
        }
        catch (Exception ex)
        {
            _logger.LogError(ex, "GetByIdAsync failed for user id: {Id}", userId);
            throw;
        }
    }

