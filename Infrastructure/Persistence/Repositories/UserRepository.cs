    private static string NormalizeSqlForUserTable(string sql)
    {
        return sql
            .Replace("INSERT INTO UserEntity", "INSERT INTO public.\"user\"")
            .Replace("UPDATE UserEntity t", "UPDATE public.\"user\" t")
            .Replace("DELETE FROM UserEntity t", "DELETE FROM public.\"user\" t")
            .Replace("FROM UserEntity t", "FROM public.\"user\" t");
    }

    private static User MapToDomain(UserEntity entity)
    {
        return new User
        {
            Id = entity.Id,
            Username = entity.Username,
            Email = entity.Email,
            PasswordHash = entity.PasswordHash,
            FullName = entity.FullName,
            ImageUrl = entity.ImageUrl,
            Role = entity.Role,
            RefreshToken = entity.RefreshToken,
            IsActive = string.Equals(entity.IsActive, "Y", StringComparison.OrdinalIgnoreCase)
        };
    }
