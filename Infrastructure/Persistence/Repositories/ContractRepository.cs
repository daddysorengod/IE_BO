    public async Task<Contract?> GetByIdAsync(long contractId, CancellationToken cancellationToken = default)
    {
        try
        {
            IQueryBuilder<Contract, object> queryBuilder = new QueryBuilder<Contract, object>();
            var sql = queryBuilder
                .SelectRaw(BuildContractSelectClause())
                .Where(c => c.Id == contractId)
                .BuildQuery();

            _logger.LogInformation("GetById contract SQL generated by IQueryBuilder: {Sql}", sql);

            using var connection = _connectionFactory.CreateOpenConnection();
            return await connection.QueryFirstOrDefaultAsync<Contract>(
                new CommandDefinition(sql, cancellationToken: cancellationToken));
        }
        catch (Exception ex)
        {
            _logger.LogError(ex, "GetByIdAsync failed for contract id: {Id}", contractId);
            throw;
        }
    }

