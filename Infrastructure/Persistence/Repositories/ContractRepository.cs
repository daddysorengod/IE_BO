    public async Task<long> InsertExportAggregateAsync(
        Contract contract,
        IReadOnlyCollection<ContractAttachment> attachments,
        IReadOnlyCollection<ContractPackage> packages,
        CancellationToken cancellationToken = default)
    {
        try
        {
            var insertContractSql = BuildInsertContractSql(contract);

            using var connection = _connectionFactory.CreateOpenConnection();
            using var transaction = connection.BeginTransaction();

            try
            {
                var contractId = await connection.ExecuteScalarAsync<long>(
                    new CommandDefinition(insertContractSql, contract, transaction: transaction, cancellationToken: cancellationToken));

                foreach (var attachment in attachments)
                {
                    attachment.ContractId = contractId;
                    var insertAttachmentSql = BuildInsertAttachmentSql(attachment);
                    await connection.ExecuteAsync(
                        new CommandDefinition(insertAttachmentSql, transaction: transaction, cancellationToken: cancellationToken));
                }

                foreach (var package in packages)
                {
                    package.ContractId = contractId;
                    var insertContractPackageSql = BuildInsertContractPackageSql(package);
                    var contractPackageId = await connection.ExecuteScalarAsync<long>(
                        new CommandDefinition(insertContractPackageSql, transaction: transaction, cancellationToken: cancellationToken));

                    var item = new ContractItem
                    {
                        ContractId = contractId,
                        ContractPackageId = contractPackageId,
                        PackageId = package.PackageId,
                        ProductId = package.ProductId,
                        SupplierId = null,
                        Quantity = package.Quantity,
                        ImportPrice = 0,
                        SellPrice = package.SellPrice,
                        TotalImportAmount = 0,
                        TotalSellAmount = package.TotalSellAmount,
                        CreatedBy = package.CreatedBy,
                        CreatedDate = package.CreatedDate
                    };

                    var insertContractItemSql = BuildInsertContractItemSql(item);
                    await connection.ExecuteAsync(
                        new CommandDefinition(insertContractItemSql, transaction: transaction, cancellationToken: cancellationToken));
                }

                transaction.Commit();
                return contractId;
            }
            catch
            {
                transaction.Rollback();
                throw;
            }
        }
        catch (Exception ex)
        {
            _logger.LogError(ex, "InsertExportAggregateAsync failed for contract no: {ContractNo}", contract.ContractNo);
            throw;
        }
    }

